export class RandomNumber {
    constructor(arr) {
        this.number = Math.floor(Math.random()*arr.length);
        return this.number
    }
}

export function RandomName(arr = boyName) {
    return arr[new RandomNumber(arr).number]
}

export class Sample {
    constructor(arr, n) {
        this.returnArray = [];
        for (let i = 0 ; i < n ; i++) {
            this.returnArray.push(arr[new RandomNumber(arr).number]);
        }
    }
}

